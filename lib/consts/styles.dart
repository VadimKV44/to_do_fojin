

import 'package:flutter/material.dart';
import 'package:to_do_fojin/consts/colors.dart';

class MainStyles {

  ///   FontWeight.w700
  static TextStyle kBlackColor1W700(double fontSize) {
    return TextStyle(
      color: MainColors.kBlackColor1,
      fontSize: fontSize,
      fontWeight: FontWeight.w700,
    );
  }

  static TextStyle kGreyColor1W700(double fontSize) {
    return TextStyle(
      color: MainColors.kGreyColor1,
      fontSize: fontSize,
      fontWeight: FontWeight.w700,
    );
  }

  ///   FontWeight.w500
  static TextStyle kBlackColor1W500(double fontSize) {
    return TextStyle(
      color: MainColors.kBlackColor1,
      fontSize: fontSize,
      fontWeight: FontWeight.w500,
    );
  }
}